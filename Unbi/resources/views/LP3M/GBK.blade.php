<p style="margin-left:18pt;">3.&nbsp;&nbsp;&nbsp;&nbsp; <strong>Tujuan dan Strategi Sistem Penjaminan Mutu Internal</strong></p>
<ol style="list-style-type:lower-alpha;">
	<li>Tujuan Sistem Penjaminan Mutu Internal</li>
</ol>

<p style="margin-left:36pt;">Seluruh civitas akademika Universitas Bali Internasional berkeyakinan bahwa SPMI bertujuan untuk :</p>

<ol style="list-style-type:lower-alpha;">
	<li style="margin-left: 57.4pt;">Menyediakan fasilitas yang memadai yang diharapkan oleh pelanggan, pengajar dan karyawan agar dapat dilaksanakan proses belajar mengajar yang baik sehingga dihasilkan kualitas lulusan yang bermutu;</li>
	<li style="margin-left: 57.4pt;">Menyelenggarakan proses pendidikan yang bersifat <em>Edutainment </em>yang sesuai dengan kurikulum berbasis kompetensi yang memenuhi persyaratan dan peraturan yang berlaku;</li>
	<li style="margin-left: 57.4pt;">Melakukan pengembangan atau perbaikan secara terus menerus disetiap area untuk meraih kepuasan pelanggan;</li>
	<li style="margin-left: 57.4pt;">Secara berkala memberikan pelatihan pada tenaga pengajar untuk meningkatkan kemampuan atau kompetensi agar dapat memberikan jasa yang bermutu dan terkini.</li>
	<li value="2">Strategi Sistem Penjaminan Mutu Internal</li>
</ol>

<p style="margin-left:36pt;">Strategi SPMI Universitas Bali Internasional Strategi di dalam melaksanakan SPMI adalah:</p>

<ol>
	<li style="margin-left: 58.5pt;">Melibatkan secara aktif semua civitas akademika sejak tahap perencanaan hingga tahap evaluasi dan tahap pengembangan SPMI.</li>
	<li style="margin-left: 58.5pt;">Melibatkan pula organisasi profesi, alumni, dunia usaha dan pemerintahan sebagai pengguna lulusan, khususnya pada tahap penetapan standar SPMI.</li>
	<li style="margin-left: 58.5pt;">Melakukan pelatihan secara terstruktur dan terencana bagi para dosen dan staf administrasi tentang SPMI, dan secara khusus pelatihan sebagai auditor internal.</li>
	<li style="margin-left: 58.5pt;">Melakukan sosialisasi tentang fungsi dan tujuan SPMI kepada para pemangku kepentingan secara periodik.</li>
</ol>

<p style="margin-left:18pt;">4.&nbsp;&nbsp;&nbsp;&nbsp; <strong>Prinsip Atau Azas Pelaksanaan Sistem Penjaminan Mutu Internal</strong></p>

<p style="margin-left:36pt;">a.&nbsp;&nbsp;&nbsp;&nbsp; Prinsip SPMI</p>

<p style="margin-left:36pt;">Untuk mencapai tujuan SPMI&nbsp; tersebut di atas dan juga untuk mewujudkan visi, misi, dan tujuan Universitas Bali Internasional, maka civitas akademika&nbsp; dalam melaksanakan SPMI pada setiap aras dalam Institusi selalu berpedoman pada prinsip:</p>

<ol>
	<li style="margin-left: 58.5pt;">Berorientasi kepada pemangku kepentingan internal dan eksternal;</li>
	<li style="margin-left: 58.5pt;">Mengutamakan kebenaran;</li>
	<li style="margin-left: 58.5pt;">Tanggungjawab sosial;</li>
	<li style="margin-left: 58.5pt;">Pengembangan kompetensi personal;</li>
	<li style="margin-left: 58.5pt;">Partisipatif dan kolegial;</li>
	<li style="margin-left: 58.5pt;">Keseragaman metode;</li>
	<li style="margin-left: 58.5pt;">Inovasi, belajar dan perbaikan secara berkelanjutan</li>
</ol>

<p style="margin-left:36pt;">b.&nbsp;&nbsp;&nbsp;&nbsp; Azas Sistem Penjaminan Mutu Internal</p>

<ol>
	<li style="margin-left: 58.5pt;">Azas akuntabilitas, yaitu bahwa dalam pelaksanaan kebijakan SPMI harus dipertanggungjawabkan secara ilmiah, terbuka dan senantiasa mengacu pada perkembangan keilmuan yang mutakhir dan dinamis.</li>
	<li style="margin-left: 58.5pt;">Azas transparansi, yaitu bahwa kebijakan SPMI dilaksanakan secara terbuka didasarkan pada tatanan dan aturan yang jelas dan senantiasa berorientasi pada rasa saling percaya untuk terselenggaranya suasana akademik yang kondusif dan menjamin terwujudnya sinergi.</li>
	<li style="margin-left: 58.5pt;">Azas kualitas, yaitu bahwa kebijakan SPMI diselenggarakan dengan senantiasa mengedepankan kualitas input, proses, output.</li>
	<li style="margin-left: 58.5pt;">Azas kebersamaan, yaitu bahwa kebijakan SPMI dilaksanakan secara terpadu, terstruktur, sistematik, komprehensif dan terarah, dengan berbasis pada visi, misi dan tujuan kelembagaan.</li>
	<li style="margin-left: 58.5pt;">Azas hukum, yaitu bahwa semua pihak yang terlibat secara langsung maupun tidak langsung dalam pelaksanaan kebijakan SPMI taat pada hukum yang berlaku yang penegakannya dijamin oleh Negara.</li>
	<li style="margin-left: 58.5pt;">Azas manfaat, yaitu bahwa kebijakan SPMI dilaksanakan untuk memberikan manfaat yang sebesar-besarnya bagi segenap sivitas akademika, institusi, bangsa dan Negara.</li>
	<li style="margin-left: 58.5pt;">Azas kesetaraan, yaitu bahwa kebijakan SPMi dilaksanakan atas dasar persamaan hak untuk menjamin terciptanya lingkungan akademik yang egaliter.</li>
	<li style="margin-left: 58.5pt;">Azas kemandirian, yaitu bahwa pelaksanaan kebijakan SPMI senantiasa didasarkan pada kemampuan institusi dengan mengandalkan segenap potensi dan sumber daya yang ada untuk mengoptimalkan kemampuan institusi yang terus berkembang secara sistematik dan terstruktur.</li>
</ol>, initial-scale=1.0">
