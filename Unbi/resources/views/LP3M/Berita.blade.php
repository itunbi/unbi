            <h2>Audit Mutu Internal</h2>
            <p>Audit Mutu Internal dilakukan oleh auditor yang terlatih dan dikelola oleh LP3M, dimana turut berkoordinasi dengan gugus mutu di Fakultas dan Program Studi. Tim AMI terdiri dari beberapa orang auditor untuk melaksanakan tugas audit sebuah unit atau program studi pada rentang waktu tertentu. Untuk mewujudkan obyektivitas, audit unit atau program studi/bagian tidak dilakukan oleh auditor yang berasal dari unit/program studi yang sama dengan asal auditor, melainkan akan dilakukan secara silang oleh auditor yang berasal dari latar belakang akademis yang agak bersamaan tetapi berasal dari program studi yang berbeda.
                Pelaksanaan AMI yang efektif, hanya dapat dilakukan bila Tim AMI Universitas Bali Internasional menguasai materi dan teknik dengan baik. Pemahaman materi dan teknik bisa didapatkan melalui pelatihan atau langkah lain. Pelatihan adalah alat yang sangat ampuh bagi Institut untuk mencapai sasaran pengembangan Sumber Daya Manusia (SDM) ke arah kinerja yang tinggi. Pelatihan AMI merupakan salah satu cara untuk menyiapkan kompetensi bagi Tim AMI.
                Untuk itu, LP3M UNBI melaksanakan Pelatihan Auditor Mutu Internal sekaligus Sosialisasi SPMI kepada Auditor dan sivitas akademik UNBI. Terkhusus pada Pelatihan Auditor (TOT), tujuannya adalah untuk memberikan pemahaman menyeluruh dan keterampilan teknis untuk menjadi auditor, khususnya auditor sistem penjaminan mutu internal UNBI. Di samping itu juga sebagai bagian dari proses sosialisasi tentang penjaminan mutu di lingkungan Universitas Bali Internasional kepada auditor lama maupun calon auditor. Sekaligus juga dilakukan Audit Mutu Internal pada 7 Program Studi. Kegiatan dilakukan pada 13-15 Oktober 2022, dimana berlangsung lancar. Pelatihan dilakukan dengan mengundang dua orang fasilitator yang berpengalaman dari kampus mitra dan LLDIKTI Wil. VIII yakni Dr. I Wayan Suardana, SST.Par.,M.Par., dan  Dr. Ir. Nyoman Utari Vibriyanti, MM. 
                Sekaligus pula dalam kegiatan dilakukan upaya Benchmarking dengan sharing session kedua Fasilitator atas pelaksanaan penjaminan mutu di instansi asal masing-masing, yakni Universitas Udayana dan Universitas Mahasaraswati. Benchmarking ini dilakukan dalam upaya mempelajari dan melakukan optimalisasi upaya penjaminan mutu di UNBI agar lebih berkualitas dan bersaing sesuai SNDIKTI dan kebutuhan jaman.
