            <h2>Sejarah Kami</h2>
            <img src="Image/unbino.PNG" alt="" class="image"> <br> <br>
            <p>Universitas Bali Internasional (UNBI) merupakan perubahan bentuk dari Institut Ilmu Kesehatan Medika Persada Bali (IIK Bali), melalui ijin Kemenristekdikti Nomor 955/KPT/I/2019 tanggal 10 Oktober 2019 tentang perubahan bentuk dari Institut menjadi Universitas yang bernaung dibawah Yayasan Anugerah Husada Bali Indonesia (AHBI).<br>
                <br>
                Saat ini UNBI dipimpin oleh mantan Rektor UNUD (2005-2014) yang sudah tidak asing lagi di dunia pendidikan. Dengan pengalaman kerja selama 46 tahun sebagai dosen dengan jabatan fungsional Guru Besar Bidang Kedokteran Universitas Udayana.<br>
                
                Universitas Bali Internasional memiliki 2 Fakultas, yaitu Fakultas Ilmu Ilmu Kesehatan sebagai UPPS dari Prodi S1 Teknologi Laboratorium Medik, S1 Administrasi Rumah Sakit, S1 Fisioterapi, S1 Manajemen Informasi Kesehatan, S1 Farmasi Klinis, S1 Kesehatan dan Keselamatan Kerja (K3), D4 Teknik Elektromedik, D4 Teknologi Laboratorium Medis dan Fakultas Bisnis Sosial Teknologi dan Humaniora sebagai UPPS Prodi S1 Psikologi, S1 Bisnis Digital, S1 Informatika, S1 Hukum, S1 Ilmu Administrasi Negara, S1 Ilmu Administrasi Niaga dan S1 Hubungan Internasional.<br>
                <br>
                Universitas Bali Internasional beralamat di Jl. Seroja Gang Jeruk No 9A Kelurahan Tonja Denpasar, dibangun dengan konsep green campus dengan fasilitas pembelajaran yang lengkap, dosen yang berkompeten, serta seluruh program studi yang telah terakreditasi.</p>
