        <h3>MISI</h3>
        <h5>Program Studi Sarjana Terapan (D-IV) Teknik Elektromedik adalah: <br>
            1. Berperan aktif dalam menciptakan sumber daya manusia dan pengembangan ilmu Teknik
            Elektromedik yang unggul dalam teknologi digital peralatan teknologi elektromedis baik di
            tingkat nasional, maupun di tingkat internasional.<br>
            2. Sebagai pusat penelitian teknologi elektromedik untuk pencapaian kompetensi evaluator dan
            teknopreneur guna meningkatkan profesionalisme tenaga teknik elektromedik.<br>
            3. Berpartisipasi aktif dalam pengabdian dan pelayanan kepada masyarakat, khususnya sebagai
            pusat informasi perkembangan teknologi elektromedik terkini.<br>
            4. Tata kelola Sumber Daya Manusia yang profesional untuk menjadi tenaga kesehatan yang
            memiliki kompetensi khusus dibidang Teknik Elektromedik.<br>
        </h5>
      <div class="program-label">D4 Teknik Elektromedik</div>
      <p>
        Fokus dalam pengembangan Program Studi Sarjana Terapan Teknik Elektromedik
Universitas Bali Internasional, melakukan pengintegrasian dengan bidang teknologi digital.
Sehingga mampu berkontribusi pada desain dan evaluasi tugas, pekerjaan, produk, dan sistem
untuk menyesuaikan kebutuhan, kemampuan dan keterbatasan manusia. Maka peranan
teknologi digital dalam pendidikan teknik elektromedik adalah untuk mengoptimalkan desain
(perancangan alat kesehatan) dan kebaruan dalam pengembangan peralatan kesehatan. Dengan
keunggulan inilah diharapkan lulusan Teknik Elektromedik mampu mandiri, berdaya saing
tinggi, dan meningkatkan kerja sama dengan institusi terkait, seperti bengkel maintanance
peralatan Elektromedik, serta Badan Kalibrasi Fasilitas Kesehatan.
